#include <bits/stdc++.h>
using namespace std;

/*  
Note: 

left shift multiplies :

example: 10 << 1, this should give 20

right shift divides :

example: 20 << 2, this should give us 5

*/

int main(int argc,char** argv) {
    int i = 10<<1;
    int j = 20>>2;
    cout <<  i << " " << endl;
    cout <<  j << " " << endl;
    return 0;
}